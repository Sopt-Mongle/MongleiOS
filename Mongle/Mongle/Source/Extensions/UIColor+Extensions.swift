import UIKit

extension UIColor {
    @nonobjc class var softGreen: UIColor {
      return UIColor(red: 115.0 / 255.0, green: 192.0 / 255.0, blue: 136.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var blur1: UIColor {
        return UIColor(white: 0, alpha: 0.35)
       }
    
    @nonobjc class var blur2: UIColor {
           return UIColor(white: 1, alpha: 0.28)
          }

    @nonobjc class var tea: UIColor {
      return UIColor(red: 105.0 / 255.0, green: 175.0 / 255.0, blue: 124.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var ice: UIColor {
      return UIColor(red: 230.0 / 255.0, green: 245.0 / 255.0, blue: 233.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var reddish: UIColor {
      return UIColor(red: 207.0 / 255.0, green: 50.0 / 255.0, blue: 50.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var veryLightPinkFive: UIColor {
      return UIColor(white: 219.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 188.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPinkSix: UIColor {
      return UIColor(white: 231.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPinkSeven: UIColor {
      return UIColor(white: 222.0 / 255.0, alpha: 1.0)
    }


}
